def groups():
    group_names = set(r['group'] for r in matches() if r['group'] != '')
    groups = {g:set() for g in group_names}
    for r in matches():
        if r['group'] != '':
            groups[r['group']].add(r['team1'])
            groups[r['group']].add(r['team2'])
    return groups

